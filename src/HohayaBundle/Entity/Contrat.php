<?php

namespace HohayaBundle\Entity;

/**
 * Contrat
 */
class Contrat
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $contenu;


    /**
     * Get id.
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set contenu.
     *
     * @param string $contenu
     *
     * @return Contrat
     */
    public function setContenu($contenu)
    {
        $this->contenu = $contenu;

        return $this;
    }

    /**
     * Get contenu.
     *
     * @return string
     */
    public function getContenu()
    {
        return $this->contenu;
    }
}
